package com.gloogie.mshpoa.model;

/**
 * Type of the sensor
 */
public class MeasureType
{
    private String code;
    private String name;

    public String getCode() {
        return code;
    }

    public void setCode(final String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }
}
